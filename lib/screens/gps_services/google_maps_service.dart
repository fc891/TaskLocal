import 'package:flutter/material.dart';
import 'package:google_maps_flutter/google_maps_flutter.dart';
import 'package:google_maps_flutter_android/google_maps_flutter_android.dart';
import 'package:tasklocal/main.dart';

class GoogleMapsService extends StatefulWidget {
  const GoogleMapsService({super.key});

  @override
  State<GoogleMapsService> createState() => _GoogleMapsServiceState();
}

class _GoogleMapsServiceState extends State<GoogleMapsService> {
  late GoogleMapController mapController;
  final LatLng _center = const LatLng(-23.5557714, -46.6395571);

  void _onMapCreated(GoogleMapController controller) {
    mapController = controller;
  }

  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      home: Scaffold(
        appBar: AppBar(
          title: Text("Sample"),
        ),
        body: GoogleMap(
          onMapCreated: _onMapCreated,
          initialCameraPosition: CameraPosition(
            target: _center,
            zoom: 11.0,
          ),
        ),
      ),
    );
  }
}
